package com.nandan.shop.dto;

import java.math.BigDecimal;

public class BalanceDetails {

	public BigDecimal amountBal;
	public BigDecimal goldBal;
	public BigDecimal silverBal;
	public BigDecimal getAmountBal() {
		return amountBal;
	}
	public void setAmountBal(BigDecimal amountBal) {
		this.amountBal = amountBal;
	}
	public BigDecimal getGoldBal() {
		return goldBal;
	}
	public void setGoldBal(BigDecimal goldBal) {
		this.goldBal = goldBal;
	}
	public BigDecimal getSilverBal() {
		return silverBal;
	}
	public void setSilverBal(BigDecimal silverBal) {
		this.silverBal = silverBal;
	}
	
	
}
